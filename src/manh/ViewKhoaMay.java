        listMT.add(new Object[]{"MA_4", "Máy 4", 1000, "PRO_VIP", "KHU VỰC 1", "Offline"});
        listMT.add(new Object[]{"MA_5", "Máy 5", 6000, "THUONG", "KHU VỰC 3", "Online"});
        listMT.add(new Object[]{"MA_6", "Máy 6", 1000, "PRO_VIP", "KHU VỰC 1", "Offline"});
