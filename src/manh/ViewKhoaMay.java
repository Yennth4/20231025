        listMT.clear();
        listMT.add(new Object[]{"MA_1", "Máy 1", 10000, "PRO_VIP", "KHU VỰC 2", tt});
        listMT.add(new Object[]{"MA_2", "Máy 2", 8500, "VIP", "KHU VỰC 2", "Online"});
